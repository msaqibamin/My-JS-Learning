// ---------- While Loop ----------
let i = 1;
while (i <= 10) {
  //   console.log(`Value of i is ${i}`);
  i = i + 2;
}

let myArr = ["Saqib", "Amin", "Wahaj", "Taseen"];
let arr = 0;
while (arr < myArr.length) {
  //   console.log(`Value of Array is ${myArr[arr]}.`);
  arr = arr + 1;
}

// ---------- Do While Loop ----------

// Aik baar loop nay chalna hi hay us kay baad condition check ho gi "do while loop main".
let score = 1;
do {
  //   console.log(`Value of Array is ${score}.`);
  score = score + 1;
} while (score <= 10);
