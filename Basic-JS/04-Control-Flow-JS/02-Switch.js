// ---------- Switch Cases ----------

let balance = 600
switch (balance) {
    case 500:
        console.log("Balance is Equal then 1000");
        break;
    case 700:
        console.log("Balance is Equal then 700");
        break;
    case 900:
        console.log("Balance is Equal then 900");
        break;

    default:
        console.log("No Match Found");
        break;
}